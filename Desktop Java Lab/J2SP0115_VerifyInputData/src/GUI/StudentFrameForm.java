    private void jTextFieldCodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCodeFocusLost
        // TODO add your handling code here:
        if (jTextFieldCode.getText().equals("")) {
            jLabelNoti.setText("Please enter student code");
        } else if (!check.ckCharCode(jTextFieldCode.getText())) {
            jLabelNoti.setText("Student code not contains special characters: @, #, $ and space");
        } else {
            jLabelNoti.setText("Code is valid");
        }
    }//GEN-LAST:event_jTextFieldCodeFocusLost

    private void jTextFieldNameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldNameFocusLost
        // TODO add your handling code here:
        if (jTextFieldName.getText().equals("")) {
            jLabelNoti.setText("Please enter Student name");
        }else if (!check.ckValidName(jTextFieldName.getText())) {
            jLabelNoti.setText("Name not be number and special characters");
        } else {
            jLabelNoti.setText("Name is valid");
        }
    }//GEN-LAST:event_jTextFieldNameFocusLost

    private void jTextFieldDateFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldDateFocusLost
        // TODO add your handling code here:
        jLabelHint.setText("");
        if (jTextFieldDate.getText().equals("")) {
            jLabelNoti.setText("Please enter Birth date");
        } else if (!check.ckValidBirthDate(jTextFieldDate.getText())) {
            jLabelNoti.setText("Birth date must be in format MM/DD/YYYY");
        } else if (!check.ckDateIsExist(jTextFieldDate.getText())) {
            jLabelNoti.setText("This date isn't exsit.");
        } else if (!check.ckBirthDate(jTextFieldDate.getText())) {
            jLabelNoti.setText("Invalid birth date");
        } else {
            jLabelNoti.setText("Date is valid");
        }
    }//GEN-LAST:event_jTextFieldDateFocusLost

